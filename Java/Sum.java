package Java;

import java.util.*;

public class Sum {
    public static void main(String args[]) {
        int a, b, s;
        Scanner sc = new Scanner(System.in);
        System.out.println("\n Enter two number- ");
        a = sc.nextInt();
        b = sc.nextInt();
        s = a + b;
        System.out.println("Sum- " + s);
    }

}
